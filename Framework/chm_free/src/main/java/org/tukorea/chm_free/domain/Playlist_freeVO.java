package org.tukorea.chm_free.domain;


public class Playlist_freeVO {
	private int playlistNumber;
	private String playlistName;
	private String playlistDescribe;
	private int playlistLikes;
	private String playlistLink;
	private String playlistPassword;
	private String playlistPhoto;
	
	
	public int getPlaylistNumber() {
		return playlistNumber;
	}
	public void setPlaylistNumber(int playlistNumber) {
		this.playlistNumber = playlistNumber;
	}
	public String getPlaylistName() {
		return playlistName;
	}
	public void setPlaylistName(String playlistName) {
		this.playlistName = playlistName;
	}
	public String getPlaylistDescribe() {
		return playlistDescribe;
	}
	public void setPlaylistDescribe(String playlistDescribe) {
		this.playlistDescribe = playlistDescribe;
	}
	public int getPlaylistLikes() {
		return playlistLikes;
	}
	public void setPlaylistLikes(int playlistLikes) {
		this.playlistLikes = playlistLikes;
	}
	public String getPlaylistLink() {
		return playlistLink;
	}
	public void setPlaylistLink(String playlistLink) {
		this.playlistLink = playlistLink;
	}
	public String getPlaylistPassword() {
		return playlistPassword;
	}
	public void setPlaylistPassword(String playlistPassword) {
		this.playlistPassword = playlistPassword;
	}
	public String getPlaylistPhoto() {
		return playlistPhoto;
	}
	public void setPlaylistPhoto(String playlistPhoto) {
		this.playlistPhoto = playlistPhoto;
	}
	
	
	
}